OUTPOST_NAME_SUFFIX = "чек ара заставасы"


def format_outpost_name(name):
    normalized_name = str(name or "").strip()
    if not normalized_name or normalized_name.endswith(f" {OUTPOST_NAME_SUFFIX}"):
        return normalized_name
    return f"{normalized_name} {OUTPOST_NAME_SUFFIX}"


def normalize_outpost_selection(unit_number, outpost_name):
    selected_name = str(outpost_name or "").strip()
    for available_name in OUTPOSTS_BY_MILITARY_UNIT.get(unit_number, ()):
        full_name = format_outpost_name(available_name)
        if selected_name in {available_name, full_name}:
            return full_name
    return None
